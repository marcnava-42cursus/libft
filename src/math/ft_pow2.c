#include "libft.h"

/**
 * @brief Calculates the square of an integer.
 *
 * This function returns the result of multiplying the given
 * integer base by itself.
 *
 * @param base The integer value to be squared.
 * @return The square of the input integer (base * base).
 */
t_int	ft_pow2(t_int base)
{
	return (base * base);
}
